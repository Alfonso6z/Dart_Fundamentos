/**
 *  Un operador es un símbolo que dice al compilador
 *  qué debe de realizar una tarea matematica, relaciínada
 *  o lógica y debe de producir un resultado
 */

main(){
  int a = 10 + 5; // + 15
  a = 20 - 10 ;   // - 10
  a = 10 * 2;     // * 20

  double b = 10 / 2; //   /   5
  b = 10.0 % 3;      //    %   1 El sobrate de la división
  b = -b;            //   -expr Es usado para cambiar el valor de la expresión
  
  int c = 10 ~/ 3;   //   ~/  3  División común y corriente, parte entera  **virgulilla

  int d = 1;
  
  d++; // incrementa 1 
  d--; // resta 1

  d += 2; // incrementa en 2 o en o que queras 
  d -= 2; // resta 2 o en lo que quieras


}